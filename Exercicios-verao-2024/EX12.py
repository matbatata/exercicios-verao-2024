D = int(input("Digite um número: "))
if D < 0 and D > 7:
    print("número invalido")
elif D == 1:
    print("Segunda-feira")
elif D == 2:
    print("Terça-feira")
elif D == 3:
    print("Quarta-feira")
elif D == 4:
    print("Quinta-feira")
elif D == 5:
    print("Senxta-feira")
elif D == 6:
    print("Sabado")
elif D == 7:
    print("Domingo")